import { readdir, writeFile } from "node:fs/promises";
import path from "node:path";

const root = process.cwd();
const imagesDirectory = path.join(root, "public", "images");
const outputFile = path.join(root, "data", "media-manifest.ts");
const allowedExtensions = new Set([".avif", ".gif", ".jpeg", ".jpg", ".png", ".webp"]);

async function collectFiles(directory) {
  const entries = await readdir(directory, { withFileTypes: true });
  const files = [];

  for (const entry of entries) {
    const fullPath = path.join(directory, entry.name);
    if (entry.isDirectory()) {
      files.push(...(await collectFiles(fullPath)));
    } else if (allowedExtensions.has(path.extname(entry.name).toLowerCase())) {
      files.push(fullPath);
    }
  }

  return files;
}

const files = (await collectFiles(imagesDirectory))
  .map((file) => `/${path.relative(path.join(root, "public"), file).replaceAll("\\", "/")}`)
  .sort((left, right) => left.localeCompare(right));

const output = `// Generated by scripts/generate-media-manifest.mjs.\nexport const mediaManifest = ${JSON.stringify(files, null, 2)} as const;\n`;
await writeFile(outputFile, output, "utf8");
console.log(`Generated ${files.length} CMS media entries.`);
